std::unique_ptr<Meter> mysql_meter{nullptr};

void register_mysql_metrics(MetricsRegistry *metrics_registry,
                                CommonLabels common_labels)
{
  assert(metrics_registry);
  mysql_meter= std::make_unique<Meter>(metrics_registry, common_labels);
  mysql_meter->Register(NAME_MYSQL_TX_DURATION, Type::Histogram);
  mysql_meter->Register(NAME_MYSQL_PROCESSED_TX_TOTAL, Type::Counter);
  mysql_meter->Register(NAME_MYSQL_PROCESSED_QUERY_TOTAL, Type::Counter);
  mysql_meter->Register(NAME_CONNECTION_COUNT, Type::Gauge);
  mysql_meter->Register(NAME_MAX_CONNECTIONS, Type::Gauge);
  mysql_meter->Register(
      NAME_MYSQL_DML_TOTAL, Type::Counter,
      {{"type",
        {"SELECT", "INSERT", "INSERT_SELECT", "UPDATE", "UPDATE_MULTI",
         "DELETE", "DELETE_MULTI", "LOAD"}}});
  mysql_meter->Register(
      NAME_MYSQL_DML_DURATION, Type::Histogram,
      {{"type",
        {"SELECT", "INSERT", "INSERT_SELECT", "UPDATE", "UPDATE_MULTI",
         "DELETE", "DELETE_MULTI", "LOAD"}}});

  // Once registered the following metrics must be collected
  mysql_meter->Collect(NAME_MAX_CONNECTIONS,
                       static_cast<double>(max_connections));
}

